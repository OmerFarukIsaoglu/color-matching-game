    private void StartButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_StartButtonActionPerformed
        
        startTime=System.currentTimeMillis();
        for (ColorBox box : boxes) {
            OyunPanel.remove(box);
        }
        boxes.clear();
        
        int SpinnerSayi=(int) Spinner.getValue();
        
        for (int i = 0; i < SpinnerSayi; i++) {
            Color renk=renkler[i];
            
            for (int j = 0; j <2; j++) {
                int RandomX=(int)(Math.random()*(600-200));
                int RandomY=(int)(Math.random()*(400-200));
                
                ColorBox box=new ColorBox(RandomX,RandomY,50,50, renk,this);
                boxes.add(box);
                OyunPanel.add(box);
            }
            OyunPanel.revalidate();
            OyunPanel.repaint();
        }
        
    }//GEN-LAST:event_StartButtonActionPerformed

    
