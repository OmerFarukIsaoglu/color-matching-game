    private void SkorKaydet(double YeniSkor){
        try{
        File file=new File("users.txt");
        BufferedReader reader = new BufferedReader(new FileReader(file));
        StringBuilder sb = new StringBuilder();
        String line;
        
        while((line=reader.readLine())!=null){
            String[] parca=line.split(";");
            if(parca[0].equalsIgnoreCase(eposta)){
                line=parca[0] + ";" + parca[1] + ";" + parca[2] + ";" + parca[3] + ";" + YeniSkor;
            }
            sb.append(line).append("\n");
        }
        reader.close();
        
        BufferedWriter writer=new BufferedWriter(new FileWriter(file));
        writer.write(sb.toString());
        writer.close();
        }
        catch (IOException e) {
        JOptionPane.showMessageDialog(null, "Dosya hatası: " + e.getMessage(), "Hata!", JOptionPane.ERROR_MESSAGE);
    }
    }
    private void Carpisma(){
         ColorBox Kutu1=null;
         ColorBox Kutu2=null;
         
         for (int i = 0; i <boxes.size(); i++) {
             for (int j =i+1; j < boxes.size(); j++) {
                 ColorBox box1=boxes.get(i);
                 ColorBox box2=boxes.get(j);
                 
                 Rectangle r1=box1.getBounds();
                 Rectangle r2=box2.getBounds();
                 
                 if(r1.intersects(r2) && box1.GetRenk().equals(box2.GetRenk())){
                     Kutu1=box1;
                     Kutu2=box2;
                     break;
                 }
             }
         }
         if(Kutu1!=null && Kutu2!=null){
             OyunPanel.remove(Kutu1);
             OyunPanel.remove(Kutu2);
             boxes.remove(Kutu1);
             boxes.remove(Kutu2);
             OyunPanel.revalidate();
             OyunPanel.repaint();
         }
         if(boxes.isEmpty()){
             long EndTime=System.currentTimeMillis();
             double seconds=(EndTime-startTime)/1000.0;
             
             int sayi=(int) Spinner.getValue();
             double normalizedScore= seconds * (5.0/sayi);
             String skor=String.format("%.1f",normalizedScore);
             
            if(enYuksekSkor == 0.0){
    enYuksekSkor = normalizedScore;
    SkorLabel.setText("En son girilen Skor:" + String.format("%.1f", enYuksekSkor));
    SkorKaydet(enYuksekSkor);
    JOptionPane.showMessageDialog(null,"Oyun Bitti!\n" +
        "Kullanıcı: " + KullaniciAdi + "\n" +
        "Süre: " + String.format("%.1f", seconds) + " saniye\n" +
        "Skor: " + skor + "\n" +
        "İlk skorunuz kaydedildi!");
} else if(normalizedScore < enYuksekSkor){
    enYuksekSkor = normalizedScore;
    SkorLabel.setText("En son girilen Skor:" + String.format("%.1f", enYuksekSkor));
    SkorKaydet(enYuksekSkor);
    JOptionPane.showMessageDialog(null,"Oyun Bitti!\n" +
        "Kullanıcı: " + KullaniciAdi + "\n" +
        "Süre: " + String.format("%.1f", seconds) + " saniye\n" +
        "Skor: " + skor + "\n" +
        "Tebrikler! Yeni rekor: " + String.format("%.1f", enYuksekSkor));
} else {
    JOptionPane.showMessageDialog(null,"Oyun Bitti!\n" +
        "Kullanıcı: " + KullaniciAdi + "\n" +
        "Süre: " + String.format("%.1f", seconds) + " saniye\n" +
        "Skor: " + skor + "\n" +
        "Maalesef rekoru geçemediniz!\n" +
        "Mevcut Rekor: " + String.format("%.1f", enYuksekSkor));
}
         }
         
     }
    private void DownButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DownButtonActionPerformed
        int Pixel=20;
        for (ColorBox box : boxes) {
        if (box.Odakli()) {
            int yeniY = box.getY() + Pixel;
            if (yeniY > OyunPanel.getHeight()- box.getHeight()) 
                yeniY = OyunPanel.getHeight() - box.getHeight();
            box.setLocation(box.getX(), yeniY);
        }
    }
        Carpisma();
    }//GEN-LAST:event_DownButtonActionPerformed

